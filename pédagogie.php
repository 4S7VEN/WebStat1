                    <br><em>Pédagogie active</em>
                    <br>La pédagogie active est le socle commun aux modèles de pédagogies alternatives. Elle part du principe que l’enfant est moteur de son éducation et de ses apprentissages grâce à ses compétences innées. La pédagogie active préconise que l’enfant construise lui-même ses savoirs en se mettant dans une posture d’expérimentation et de recherche. Il est par conséquent actif et non passif dans son parcours scolaire.
                    <br>Contrairement aux principes de la pédagogie traditionnelle, la pédagogie active considère que ce n’est pas l’adulte seul qui détient le savoir : l’enseignant est plutôt  un facilitateur et un médiateur  dans l’acquisition des connaissances par l’enfant et dans son épanouissement dans le groupe.
